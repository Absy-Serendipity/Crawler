class Webtoon:
    def setWebtoon(self, platform, day, id, title, author, serializedDay, url, imageUrl, genre, summary):
        self.platform = platform
        self.day = day
        self.id = id
        self.title = title
        self.author = author
        self.serializedDay = serializedDay
        self.url = url
        self.imageUrl = imageUrl
        self.genre = genre
        self.summary = summary